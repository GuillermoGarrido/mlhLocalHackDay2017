
package hackathon;

public class Room 
{
   String name;
   int size;
   
   public Room(String name, int size)
   {
      this.name = name;
      this.size = size;
   }
   
   public int getSize()
   {
      return size;
   }
   
   public String getName()
   {
      return name;
   }
   
}
